export function formatSpreadPercentMajor(major: number): string {
	return `${major.toFixed(2)}%`;
}

export function formatAssumedBaseRateMajor(major: number): string {
	return `${major.toFixed(2)} BRL per USD`;
}

export function formatEffectiveRateMajor(major: number): string {
	return `${major.toFixed(2)} BRL per USD`;
}
